    private void b2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b2ActionPerformed
        // TODO add your handling code here:
        
        try
        {
            String rollno=c1.getSelectedItem().toString();
            String name=t2.getText();
            String branch=c2.getText();
            String semester=c3.getText();
            String mobileno=t3.getText();
            if(mobileno.length()!=10)
            {
                JOptionPane.showMessageDialog(null,"Enter a valid mobile number....");
            }
            String pname=t4.getText();
            String pmobileno=t5.getText();
            if(pmobileno.length()!=10)
            {
                JOptionPane.showMessageDialog(null,"Enter a valid mobile number....");
            }
            String address=t8.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/jproject1","root","");
            
            String q="update addstudent set Name='"+name+"',Branch='"+branch+"',Semester='"+semester+"',MobileNo='"+mobileno+"',PName='"+pname+"',PMobileNo='"+pmobileno+"',Address='"+address+"' where RollNo='"+rollno+"'";
            
            Statement stmt=con.createStatement();
            int rs=stmt.executeUpdate(q);
            
            if(rs!=0)
                System.out.println("Updated");
            
            con.close();
        }catch(Exception ex)
        {
            System.out.println(ex);
        }
    }//GEN-LAST:event_b2ActionPerformed

