    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try
        {
            String rollno=c1.getSelectedItem().toString();
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/jproject1","root","");
            
            String q="select * from addstudent where RollNo='"+rollno+"'";
            Statement stmt=con.createStatement();
            
            ResultSet rs=stmt.executeQuery(q);
            
            while(rs.next())
            {
                t2.setText(rs.getString("Name"));
                c2.setText(rs.getString("Branch"));
                c3.setText(rs.getString("Semester"));
                t3.setText(rs.getString("MobileNo"));
                t4.setText(rs.getString("PName"));
                t5.setText(rs.getString("PMobileNo"));
                t8.setText(rs.getString("Address"));
            }
            con.close();
        }catch(Exception ex)
        {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

